//Dodawanie
let liczba1 = prompt("Podaj pierwszą liczbę")
let liczba2 = prompt("Podaj drugą liczbę")

liczba1 = parseInt(liczba1);
liczba2 = parseInt(liczba2);

let wynik = liczba1 + liczba2

//wyświetlanie wyniku

document.write(liczba1 + "+" + liczba2 + "=" + wynik + "<br>");

//Odejmowanie
let wynik2 = liczba1 - liczba2

document.write(liczba1 + "-" + liczba2 + "=" + wynik2 + "<br>");

//Mnożenie
let wynik3 = liczba1 * liczba2

document.write(liczba1 + "*" + liczba2 + "=" + wynik3 + "<br>");

//Dzielenie
let wynik4 = liczba1 / liczba2

document.write(liczba1 + "/" + liczba2 + "=" + wynik4 + "<br>");


